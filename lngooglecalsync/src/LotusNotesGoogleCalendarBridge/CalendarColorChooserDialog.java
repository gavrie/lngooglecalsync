public String getColor() {
    String H_RED,H_GREEN,H_BLUE;
    Color color1 = jColorChooser1.getColor();

    H_RED = Integer.toHexString(color1.getRed()).toUpperCase();
    H_GREEN = Integer.toHexString(color1.getGreen()).toUpperCase();
    H_BLUE = Integer.toHexString(color1.getBlue()).toUpperCase();

    String chosenColor = "#" + H_RED + H_GREEN + H_BLUE;

    return chosenColor;
    }


